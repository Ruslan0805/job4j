package ru.job4j.calculate;
/**
 * Calculate
 *
 * @author Ruslan Yusov (ruslan0805@mail.ru)
 */
public class Calculate {
	/**
	 * Method add.
	 * @param first - first.
	 * @param second - second.
	 * @return first + second.
	 */
	public double add(double first, double second) {
		return first + second;
	}
	/**
	 * Method subtract.
	 * @param first - first.
	 * @param second - second.
	 * @return first - second.
	 */
	public double subtract(double first, double second) {
		return first - second;
	}
	/**
	 * Method div.
	 * @param first - first.
	 * @param second - second.
	 * @return first / second.
	 */
	public double div(double first, double second) {
		return first / second;
	}
	/**
	 * Method multiple.
	 * @param first - first.
	 * @param second - second.
	 * @return first * second.
	 */
	public double multiple(double first, double second) {
		return first * second;
	}
}
